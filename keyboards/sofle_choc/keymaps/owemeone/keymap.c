#include "raw_hid.h"
bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case FOO:
            dprint("In raw button pressed\n");
            // dprintf("Record %s\n", record->event.pressed);
            if (record->event.pressed==1) {
                dprintf("In pressed\n");
                // Do something when pressed
                uint8_t report[32];
                memset(report, 0, 32);

                report[0] = 0x9B;
                raw_hid_send(report, 32);
                dprintf("Sent raw hid\n");
            } else {
                dprintf("In released\n");
            }
            return false; // Skip all further processing of this key
        case KC_ENTER:
            // Play a tone when enter is pressed
            if (record->event.pressed) {
                dprintf("Enter pressed\n");
            }
            return true; // Let QMK send the enter press/release events
        default:
            dprintf("Every key");
            return true; // Process all other keycodes normally
    }
}

