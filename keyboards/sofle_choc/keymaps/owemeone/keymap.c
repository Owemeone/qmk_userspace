void oled_render_boot(bool bootloader) {
    oled_clear();
    for (int i = 0; i < 4; i++) {
        oled_set_cursor(0, i);
        if (bootloader) {
            oled_write_ln_P(PSTR("Awaiting New Firmware"), false);
        } else {
            oled_write_ln_P(PSTR("Rebooting "), false);
        }
    }

    oled_render_dirty(true);
}

bool shutdown_user(bool jump_to_bootloader) {
    oled_render_boot(jump_to_bootloader);
    return false;
}

